package com.selfstudy.interview;

public class OneToOneThousand {

	public static void main(String[] args) {
		
		// first in the for loop , we initialize i = 0
		//then create a termination condition, that if true we will execute the loop
		//after the loop executes then the increment executes 
		//we loop back to the termination condition 
	for(int i = 1; i <= 1000; i++ ) {
		System.out.println(i);
	}


	
	}
	
}
