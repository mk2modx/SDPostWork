package com.selfstudy.jthw;

public class PostIncrement {

	public static void main(String[] args) {
		
		int total = 0;
		System.out.println(" Total Declared : " + total);
		
		int count = 0;
		System.out.println(" Count Declared : " + count);
		
		
		total = count++;
		System.out.println(" Total after reassignment : " + total);
		System.out.println(" Count after reassignment : " + count);
		
	
		
		
		
	}
	
}
